"""Phase 4: semantic SDR scaling benchmark.

Mechanism under test:
  Semantic SDR must preserve semantic structure as vocabulary grows to
  tens/hundreds of thousands of tokens.

This benchmark intentionally tests the SDR encoding layer with an offline
semantic prior. It does not claim that PMI from a tiny corpus can discover
large-vocab semantics by itself.
"""
import argparse
import json
import math
import os
import sys
import time
import tracemalloc
from typing import Dict, List, Tuple

import numpy as np

sys.path.insert(0, os.path.abspath(os.path.join(os.path.dirname(__file__), "..", "..")))

from lsl import SemanticSDREncoder, hamming_overlap


DEFAULT_SIZES = (1000, 10000, 100000)


def generate_semantic_prior(
    vocab_size: int,
    n_clusters: int = 128,
    embed_dim: int = 64,
    noise: float = 0.08,
    seed: int = 42,
) -> Tuple[np.ndarray, np.ndarray]:
    """Generate a large offline semantic prior with known cluster structure."""
    rng = np.random.default_rng(seed)
    n_clusters = min(int(n_clusters), int(vocab_size))

    prototypes = rng.standard_normal((n_clusters, embed_dim)).astype(np.float32)
    prototypes /= np.linalg.norm(prototypes, axis=1, keepdims=True) + 1e-9

    labels = (np.arange(vocab_size, dtype=np.int32) % n_clusters).astype(np.int32)
    embeddings = prototypes[labels]
    embeddings = embeddings + noise * rng.standard_normal(
        (vocab_size, embed_dim)
    ).astype(np.float32)
    embeddings /= np.linalg.norm(embeddings, axis=1, keepdims=True) + 1e-9
    return embeddings.astype(np.float32), labels


def cluster_members(vocab_size: int, n_clusters: int) -> List[np.ndarray]:
    """Return balanced token-id arrays for labels generated by modulo."""
    return [
        np.arange(cluster_id, vocab_size, n_clusters, dtype=np.int32)
        for cluster_id in range(n_clusters)
    ]


def sample_pairs(
    vocab_size: int,
    labels: np.ndarray,
    n_pairs: int,
    seed: int,
) -> Tuple[List[Tuple[int, int]], List[Tuple[int, int]]]:
    """Sample related same-cluster pairs and unrelated different-cluster pairs."""
    rng = np.random.default_rng(seed)
    n_clusters = int(labels.max()) + 1
    members = cluster_members(vocab_size, n_clusters)

    related: List[Tuple[int, int]] = []
    unrelated: List[Tuple[int, int]] = []

    while len(related) < n_pairs:
        cluster_id = int(rng.integers(n_clusters))
        tokens = members[cluster_id]
        if len(tokens) < 2:
            continue
        a, b = rng.choice(tokens, size=2, replace=False)
        related.append((int(a), int(b)))

    while len(unrelated) < n_pairs:
        a = int(rng.integers(vocab_size))
        b = int(rng.integers(vocab_size))
        if labels[a] != labels[b]:
            unrelated.append((a, b))

    return related, unrelated


def retrieval_accuracy(
    enc: SemanticSDREncoder,
    labels: np.ndarray,
    n_queries: int,
    negatives_per_query: int,
    seed: int,
) -> float:
    """Local semantic retrieval: choose same-cluster token from distractors."""
    rng = np.random.default_rng(seed)
    vocab_size = int(labels.shape[0])
    n_clusters = int(labels.max()) + 1
    members = cluster_members(vocab_size, n_clusters)

    correct = 0
    for _ in range(n_queries):
        query = int(rng.integers(vocab_size))
        cluster_id = int(labels[query])
        positives = members[cluster_id]
        if len(positives) < 2:
            continue

        target = int(rng.choice(positives[positives != query]))
        candidates = [target]
        while len(candidates) < negatives_per_query + 1:
            candidate = int(rng.integers(vocab_size))
            if labels[candidate] != cluster_id:
                candidates.append(candidate)
        rng.shuffle(candidates)

        query_sdr = enc.encode(query)
        scores = [
            hamming_overlap(query_sdr, enc.encode(candidate))
            for candidate in candidates
        ]
        correct += int(candidates[int(np.argmax(scores))] == target)

    return correct / max(1, n_queries)


def collision_rate(
    enc: SemanticSDREncoder,
    vocab_size: int,
    sample_size: int,
    seed: int,
) -> float:
    """Estimate exact SDR collision rate on a deterministic sample."""
    rng = np.random.default_rng(seed)
    n = min(sample_size, vocab_size)
    token_ids = rng.choice(vocab_size, size=n, replace=False)
    seen = set()
    collisions = 0
    for token_id in token_ids:
        active = tuple(np.flatnonzero(enc.encode(int(token_id)) > 0.5).tolist())
        if active in seen:
            collisions += 1
        else:
            seen.add(active)
    return collisions / max(1, n)


def evaluate_scale(
    vocab_size: int,
    n_clusters: int = 128,
    embed_dim: int = 64,
    sdr_dim: int = 1024,
    sparsity: float = 0.02,
    noise: float = 0.08,
    n_pairs: int = 400,
    n_queries: int = 200,
    negatives_per_query: int = 31,
    collision_sample: int = 2048,
    seed: int = 42,
) -> Dict[str, float]:
    """Evaluate semantic SDR preservation at one vocabulary size."""
    print(f"\n{'=' * 72}")
    print(f"Semantic SDR scale test: vocab={vocab_size:,}")
    print(f"{'=' * 72}")

    tracemalloc.start()
    t0 = time.perf_counter()

    embeddings, labels = generate_semantic_prior(
        vocab_size=vocab_size,
        n_clusters=n_clusters,
        embed_dim=embed_dim,
        noise=noise,
        seed=seed,
    )
    enc = SemanticSDREncoder(
        vocab_size=vocab_size,
        sdr_dim=sdr_dim,
        sparsity=sparsity,
        embed_dim=embed_dim,
        seed=seed,
    )
    loaded = enc.load_embedding_matrix(embeddings)
    setup_time = time.perf_counter() - t0

    related_pairs, random_pairs = sample_pairs(vocab_size, labels, n_pairs, seed + 1)
    related = [enc.semantic_overlap(a, b) for a, b in related_pairs]
    random = [enc.semantic_overlap(a, b) for a, b in random_pairs]
    mean_related = float(np.mean(related))
    mean_random = float(np.mean(random))
    ratio = mean_related / max(mean_random, 0.1)

    retrieval = retrieval_accuracy(
        enc,
        labels,
        n_queries=n_queries,
        negatives_per_query=negatives_per_query,
        seed=seed + 2,
    )
    collisions = collision_rate(
        enc,
        vocab_size=vocab_size,
        sample_size=collision_sample,
        seed=seed + 3,
    )

    current, peak = tracemalloc.get_traced_memory()
    tracemalloc.stop()
    peak_mb = peak / (1024 * 1024)
    total_time = time.perf_counter() - t0

    capacity_bits = enc.capacity_log2()
    vocab_bits = math.log2(max(vocab_size, 1))
    capacity_margin = capacity_bits - vocab_bits

    print(f"Loaded embeddings: {loaded:,}")
    print(f"SDR: d={enc.sdr_dim}, k={enc.k}, sparsity={enc.actual_sparsity():.3f}")
    print(f"Related overlap: {mean_related:.2f} bits")
    print(f"Random overlap:  {mean_random:.2f} bits")
    print(f"Overlap ratio:   {ratio:.2f}x")
    print(f"Retrieval acc:   {100 * retrieval:.1f}%")
    print(f"Collision rate:  {100 * collisions:.2f}% on sample")
    print(f"Capacity margin: {capacity_margin:.1f} bits over vocab")
    print(f"Time: setup={setup_time:.2f}s total={total_time:.2f}s")
    print(f"Peak memory:     {peak_mb:.1f} MB")

    return {
        "vocab_size": float(vocab_size),
        "loaded": float(loaded),
        "related_overlap": mean_related,
        "random_overlap": mean_random,
        "overlap_ratio": ratio,
        "retrieval_accuracy": retrieval,
        "collision_rate": collisions,
        "capacity_bits": capacity_bits,
        "capacity_margin_bits": capacity_margin,
        "setup_time": setup_time,
        "total_time": total_time,
        "peak_memory_mb": peak_mb,
    }


def passes(result: Dict[str, float]) -> bool:
    """Strict per-scale success criteria for mechanism #1."""
    vocab_size = int(result["vocab_size"])
    if vocab_size >= 1000000:
        ratio_target = 5.0
        retrieval_target = 0.90
        memory_target = 2048.0
        time_target = 180.0
    elif vocab_size >= 100000:
        ratio_target = 10.0
        retrieval_target = 0.95
        memory_target = 1024.0
        time_target = 90.0
    else:
        ratio_target = 3.0
        retrieval_target = 0.90
        memory_target = 256.0
        time_target = 30.0
    collision_target = 0.001

    return (
        result["overlap_ratio"] >= ratio_target
        and result["retrieval_accuracy"] >= retrieval_target
        and result["collision_rate"] <= collision_target
        and result["capacity_margin_bits"] >= 100.0
        and result["peak_memory_mb"] <= memory_target
        and result["total_time"] <= time_target
    )


def random_prior_ablation(
    vocab_size: int = 10000,
    n_clusters: int = 128,
    embed_dim: int = 64,
    sdr_dim: int = 1024,
    sparsity: float = 0.02,
    n_pairs: int = 200,
    n_queries: int = 100,
    negatives_per_query: int = 31,
    seed: int = 123,
) -> Dict[str, float]:
    """Verify that unstructured embeddings do not pass semantic scaling."""
    print(f"\n{'=' * 72}")
    print(f"Random-prior ablation: vocab={vocab_size:,}")
    print(f"{'=' * 72}")

    rng = np.random.default_rng(seed)
    labels = (np.arange(vocab_size, dtype=np.int32) % min(n_clusters, vocab_size))
    embeddings = rng.standard_normal((vocab_size, embed_dim)).astype(np.float32)
    embeddings /= np.linalg.norm(embeddings, axis=1, keepdims=True) + 1e-9

    enc = SemanticSDREncoder(
        vocab_size=vocab_size,
        sdr_dim=sdr_dim,
        sparsity=sparsity,
        embed_dim=embed_dim,
        seed=seed,
    )
    enc.load_embedding_matrix(embeddings)

    related_pairs, random_pairs = sample_pairs(vocab_size, labels, n_pairs, seed + 1)
    related = [enc.semantic_overlap(a, b) for a, b in related_pairs]
    random = [enc.semantic_overlap(a, b) for a, b in random_pairs]
    mean_related = float(np.mean(related))
    mean_random = float(np.mean(random))
    ratio = mean_related / max(mean_random, 0.1)
    retrieval = retrieval_accuracy(
        enc,
        labels,
        n_queries=n_queries,
        negatives_per_query=negatives_per_query,
        seed=seed + 2,
    )

    print(f"Related overlap: {mean_related:.2f} bits")
    print(f"Random overlap:  {mean_random:.2f} bits")
    print(f"Overlap ratio:   {ratio:.2f}x")
    print(f"Retrieval acc:   {100 * retrieval:.1f}%")

    ok = ratio <= 1.50 and retrieval <= 0.20
    print(f"Ablation status: {'PASS' if ok else 'FAIL'}")
    return {
        "overlap_ratio": ratio,
        "retrieval_accuracy": retrieval,
        "success": float(ok),
    }


def permuted_prior_ablation(
    vocab_size: int = 10000,
    n_clusters: int = 128,
    embed_dim: int = 64,
    sdr_dim: int = 1024,
    sparsity: float = 0.02,
    n_pairs: int = 200,
    n_queries: int = 100,
    negatives_per_query: int = 31,
    seed: int = 321,
) -> Dict[str, float]:
    """Verify structured embeddings fail when detached from token semantics."""
    print(f"\n{'=' * 72}")
    print(f"Permuted-prior ablation: vocab={vocab_size:,}")
    print(f"{'=' * 72}")

    embeddings, labels = generate_semantic_prior(
        vocab_size=vocab_size,
        n_clusters=n_clusters,
        embed_dim=embed_dim,
        seed=seed,
    )
    rng = np.random.default_rng(seed + 1)
    embeddings = embeddings[rng.permutation(vocab_size)]

    enc = SemanticSDREncoder(
        vocab_size=vocab_size,
        sdr_dim=sdr_dim,
        sparsity=sparsity,
        embed_dim=embed_dim,
        seed=seed,
    )
    enc.load_embedding_matrix(embeddings)

    related_pairs, random_pairs = sample_pairs(vocab_size, labels, n_pairs, seed + 2)
    related = [enc.semantic_overlap(a, b) for a, b in related_pairs]
    random = [enc.semantic_overlap(a, b) for a, b in random_pairs]
    mean_related = float(np.mean(related))
    mean_random = float(np.mean(random))
    ratio = mean_related / max(mean_random, 0.1)
    retrieval = retrieval_accuracy(
        enc,
        labels,
        n_queries=n_queries,
        negatives_per_query=negatives_per_query,
        seed=seed + 3,
    )

    print(f"Related overlap: {mean_related:.2f} bits")
    print(f"Random overlap:  {mean_random:.2f} bits")
    print(f"Overlap ratio:   {ratio:.2f}x")
    print(f"Retrieval acc:   {100 * retrieval:.1f}%")

    ok = ratio <= 1.50 and retrieval <= 0.20
    print(f"Ablation status: {'PASS' if ok else 'FAIL'}")
    return {
        "overlap_ratio": ratio,
        "retrieval_accuracy": retrieval,
        "success": float(ok),
    }


def print_summary(results: List[Dict[str, float]]) -> None:
    print(f"\n{'=' * 96}")
    print("SEMANTIC SDR SCALING SUMMARY")
    print(f"{'=' * 96}")
    print(
        f"{'Vocab':>10} {'Ratio':>8} {'Retrieval':>10} {'Collision':>10} "
        f"{'CapMargin':>10} {'Time(s)':>8} {'RAM(MB)':>8} {'Status':>8}"
    )
    print("-" * 96)
    for result in results:
        status = "PASS" if passes(result) else "FAIL"
        print(
            f"{int(result['vocab_size']):>10,} "
            f"{result['overlap_ratio']:>7.2f}x "
            f"{100 * result['retrieval_accuracy']:>9.1f}% "
            f"{100 * result['collision_rate']:>9.2f}% "
            f"{result['capacity_margin_bits']:>10.1f} "
            f"{result['total_time']:>8.2f} "
            f"{result['peak_memory_mb']:>8.1f} "
            f"{status:>8}"
        )
    print("-" * 96)


def parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser(description=__doc__)
    parser.add_argument(
        "--sizes",
        nargs="+",
        type=int,
        default=list(DEFAULT_SIZES),
        help="Vocabulary sizes to test.",
    )
    parser.add_argument("--clusters", type=int, default=128)
    parser.add_argument("--embed-dim", type=int, default=64)
    parser.add_argument("--sdr-dim", type=int, default=1024)
    parser.add_argument("--sparsity", type=float, default=0.02)
    parser.add_argument("--noise", type=float, default=0.08)
    parser.add_argument("--pairs", type=int, default=400)
    parser.add_argument("--queries", type=int, default=200)
    parser.add_argument("--negatives", type=int, default=31)
    parser.add_argument("--collision-sample", type=int, default=2048)
    parser.add_argument("--ablation-size", type=int, default=10000)
    parser.add_argument("--skip-ablation", action="store_true")
    parser.add_argument("--skip-permuted-ablation", action="store_true")
    parser.add_argument("--json-output", type=str, default=None)
    parser.add_argument("--seed", type=int, default=42)
    return parser.parse_args()


def main() -> int:
    args = parse_args()
    print("Phase 4: Semantic SDR scaling")
    print("Mechanism #1: semantic structure preservation at large vocabulary")

    results = [
        evaluate_scale(
            vocab_size=size,
            n_clusters=args.clusters,
            embed_dim=args.embed_dim,
            sdr_dim=args.sdr_dim,
            sparsity=args.sparsity,
            noise=args.noise,
            n_pairs=args.pairs,
            n_queries=args.queries,
            negatives_per_query=args.negatives,
            collision_sample=args.collision_sample,
            seed=args.seed,
        )
        for size in args.sizes
    ]

    print_summary(results)

    ablation_ok = True
    random_ablation = None
    permuted_ablation = None
    if not args.skip_ablation:
        random_ablation = random_prior_ablation(
            vocab_size=args.ablation_size,
            n_clusters=args.clusters,
            embed_dim=args.embed_dim,
            sdr_dim=args.sdr_dim,
            sparsity=args.sparsity,
            n_pairs=max(100, args.pairs // 2),
            n_queries=max(50, args.queries // 2),
            negatives_per_query=args.negatives,
            seed=args.seed + 100,
        )
        ablation_ok = bool(random_ablation["success"])
    if not args.skip_ablation and not args.skip_permuted_ablation:
        permuted_ablation = permuted_prior_ablation(
            vocab_size=args.ablation_size,
            n_clusters=args.clusters,
            embed_dim=args.embed_dim,
            sdr_dim=args.sdr_dim,
            sparsity=args.sparsity,
            n_pairs=max(100, args.pairs // 2),
            n_queries=max(50, args.queries // 2),
            negatives_per_query=args.negatives,
            seed=args.seed + 200,
        )
        ablation_ok = ablation_ok and bool(permuted_ablation["success"])

    ok = all(passes(result) for result in results) and ablation_ok
    print(f"\nOverall: {'PASS' if ok else 'FAIL'}")
    payload = {
        "benchmark": "semantic_sdr_scaling",
        "success": bool(ok),
        "results": results,
        "ablation_success": bool(ablation_ok),
        "random_ablation": random_ablation,
        "permuted_ablation": permuted_ablation,
    }
    if args.json_output:
        with open(args.json_output, "w", encoding="utf-8") as f:
            json.dump(payload, f, indent=2)
    return 0 if ok else 1


if __name__ == "__main__":
    raise SystemExit(main())
